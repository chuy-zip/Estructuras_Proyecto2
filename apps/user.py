class User:
    def __init__(self, user_name, user_password, user_age, prefers_nintendo, prefers_pc, prefers_mobile,
                 prefers_xbox, prefers_playstation, prefers_multiplayer, played_games, favorite_games):
        self.user_name = user_name
        self.user_password = user_password
        self.user_age = user_age
        self.prefers_nintendo = prefers_nintendo
        self.prefers_pc = prefers_pc
        self.prefers_mobile = prefers_mobile
        self.prefers_xbox = prefers_xbox
        self.prefers_playstation = prefers_playstation
        self.prefers_multiplayer = prefers_multiplayer
        self.played_games = played_games
        self.favorite_games = favorite_games
